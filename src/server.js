function buildAbsolutePdfUrl(pdfUrl) {
  try {
    return new URL(pdfUrl, config.app.baseUrl).toString();
  } catch (_error) {
    return `${String(config.app.baseUrl || '').replace(/\/+$/, '')}${pdfUrl}`;
  }
}

function getProjectRefForEmailSubject(mom) {
  const projectNo = String(mom.projectNoWorkOrderNo || '').trim();
  if (projectNo) {
    return projectNo.split('/')[0].trim() || projectNo;
  }
  const projectName = String(mom.projectName || '').trim();
  return projectName || 'Project';
}

function formatMeetingDateForSubject(rawDate) {
  const value = String(rawDate || '').trim();
  const match = /^(\d{4})-(\d{2})-(\d{2})$/.exec(value);
  if (match) {
    return `${match[1]}/${match[2]}/${match[3]}`;
  }
  return value || new Date().toISOString().slice(0, 10).replace(/-/g, '/');
}

function formatMeetingDateForBody(rawDate) {
  const value = String(rawDate || '').trim();
  const match = /^(\d{4})-(\d{2})-(\d{2})$/.exec(value);
  if (match) {
    const parsed = new Date(Number(match[1]), Number(match[2]) - 1, Number(match[3]));
    return parsed.toLocaleDateString('en-GB', {
      day: 'numeric',
      month: 'long',
      year: 'numeric'
    });
  }
  const parsed = new Date(value);
  if (!value || Number.isNaN(parsed.getTime())) {
    return value || '-';
  }
  return parsed.toLocaleDateString('en-GB', {
    day: 'numeric',
    month: 'long',
    year: 'numeric'
  });
}

function encodeOutlookQueryComponent(value) {
  return encodeURIComponent(String(value || '')).replace(/[!'()*]/g, (char) => {
    return `%${char.charCodeAt(0).toString(16).toUpperCase()}`;
  });
}

function buildOutlookComposeUrlDesktop({ to = '', cc = '', subject = '', body = '' }) {
  const queryParts = [];
  const trimmedTo = String(to || '').trim();
  const trimmedCc = String(cc || '').trim();
  if (trimmedTo) {
    queryParts.push(`to=${encodeOutlookQueryComponent(trimmedTo)}`);
  }
  if (trimmedCc) {
    queryParts.push(`cc=${encodeOutlookQueryComponent(trimmedCc)}`);
  }
  queryParts.push(`subject=${encodeOutlookQueryComponent(subject)}`);
  queryParts.push(`body=${encodeOutlookQueryComponent(body)}`);
  return `https://outlook.office.com/mail/deeplink/compose?${queryParts.join('&')}`;
}

function buildOutlookComposeUrlMobile({ to = '', cc = '', subject = '', body = '' }) {
  // Use deep-link compose endpoint for mobile too; /mail/?rru=compose is unreliable on iOS.
  return buildOutlookComposeUrlDesktop({ to, cc, subject, body });
}

function buildOutlookAppComposeUrl({ to = '', cc = '', subject = '', body = '' }) {
  const queryParts = [];
  const trimmedTo = String(to || '').trim();
  const trimmedCc = String(cc || '').trim();
  if (trimmedTo) {
    queryParts.push(`to=${encodeOutlookQueryComponent(trimmedTo)}`);
  }
  if (trimmedCc) {
    queryParts.push(`cc=${encodeOutlookQueryComponent(trimmedCc)}`);
  }
  queryParts.push(`subject=${encodeOutlookQueryComponent(subject)}`);
  queryParts.push(`body=${encodeOutlookQueryComponent(body)}`);
  return `ms-outlook://compose?${queryParts.join('&')}`;
}

function buildEmailDraftPayload({ mom, options, pdfUrl, pdfFileName = '' }) {
  const to = String(options.emailTo || '').trim();
  const cc = String(options.emailCc || '').trim();
  const projectRef = getProjectRefForEmailSubject(mom);
  const subject =
    String(options.emailSubject || '').trim() ||
    `MOM - Project ${projectRef} - ${formatMeetingDateForSubject(mom.meetingDate)}`;
  const pdfAbsoluteUrl = buildAbsolutePdfUrl(pdfUrl);
  const pdfFilePath = pdfFileName ? path.join(config.app.generatedDir, pdfFileName) : '';

  const meetingTitle = String(mom.meetingTitle || '-').trim() || '-';
  const meetingDate = formatMeetingDateForBody(mom.meetingDate);
  const meetingTime = String(mom.meetingTime || '-').trim() || '-';
  const meetingLocation = String(mom.meetingLocation || '-').trim() || '-';

  const bodyText = [
    'Dear Sir / Madam,',
    '',
    `Please find the Minutes of Meeting (MoM) for the project ${projectRef}, held as per the details below.`,
    '',
    `Project: ${projectRef}`,
    `Meeting Title: ${meetingTitle}`,
    `Meeting Date: ${meetingDate}`,
    `Meeting Time: ${meetingTime}`,
    `Meeting Location: ${meetingLocation}`,
    '',
    'The detailed Minutes of Meeting are attached in the PDF for your reference.',
    'For convenience, you may also access the document using the link below:',
    `${pdfAbsoluteUrl}`,
    '',
    'Please review the document and feel free to let us know if any clarifications or additions are required.',
    'Best regards,',
    'ETPL_AI MoM System'
  ].join('\r\n');
  // Keep deeplink body concise to improve mobile browser reliability.
  const composeBodyText = [
    'Dear Sir / Madam,',
    `Please find the Minutes of Meeting (MoM) for project ${projectRef}.`,
    `Meeting Date: ${meetingDate}`,
    `Meeting Time: ${meetingTime}`,
    `Meeting Location: ${meetingLocation}`,
    `PDF Link: ${pdfAbsoluteUrl}`,
    'Best regards,',
    'ETPL_AI MoM System'
  ].join('\r\n');
  const bodyHtml = buildGraphBodyHtml({
    bodyText,
    pdfUrl: pdfAbsoluteUrl
  });

  return {
    to,
    cc,
    subject,
    bodyText,
    composeBodyText,
    bodyHtml,
    pdfAbsoluteUrl,
    pdfFileName: String(pdfFileName || '').trim(),
    pdfFilePath,
    projectRef
  };
}

function buildOutlookDraftFromPayload(payload, warning = '') {
  const to = String(payload.to || '').trim();
  const cc = String(payload.cc || '').trim();
  const subject = String(payload.subject || '').trim();
  const body = String(payload.composeBodyText || payload.bodyText || '').trim();
  return {
    mode: 'outlook-draft',
    to,
    cc,
    subject,
    body,
    projectRef: payload.projectRef,
    pdfAbsoluteUrl: payload.pdfAbsoluteUrl,
    outlookComposeUrl: buildOutlookComposeUrlDesktop({ to, cc, subject, body }),
    outlookComposeMobileUrl: buildOutlookComposeUrlMobile({ to, cc, subject, body }),
    outlookAppComposeUrl: buildOutlookAppComposeUrl({ to, cc, subject, body }),
    attachmentAutoSupported: false,
    attachmentNote:
      warning ||
      'Attachment cannot be auto-added by browser deeplink. The generated PDF is opened for manual attachment.'
  };
}

async function buildEmailDraft({ mom, options, pdfUrl, pdfFileName = '' }) {
  const payload = buildEmailDraftPayload({
    mom,
    options,
    pdfUrl,
    pdfFileName
  });

  if (!isGraphDraftConfigured(config)) {
    return buildOutlookDraftFromPayload(payload);
  }

  try {
    const graphDraft = await createGraphDraft(config, {
      to: payload.to,
      cc: payload.cc,
      subject: payload.subject,
      bodyText: payload.bodyText,
      bodyHtml: payload.bodyHtml,
      pdfFileName: payload.pdfFileName,
      pdfFilePath: payload.pdfFilePath
    });

    return {
      ...graphDraft,
      to: payload.to,
      cc: payload.cc,
      subject: payload.subject,
      body: payload.bodyText,
      projectRef: payload.projectRef,
      pdfAbsoluteUrl: payload.pdfAbsoluteUrl,
      // Keep deeplink URLs as hard fallback.
      outlookComposeUrl: buildOutlookComposeUrlDesktop({
        to: payload.to,
        cc: payload.cc,
        subject: payload.subject,
        body: payload.bodyText
      }),
      outlookComposeMobileUrl: buildOutlookComposeUrlMobile({
        to: payload.to,
        cc: payload.cc,
        subject: payload.subject,
        body: payload.bodyText
      }),
      outlookAppComposeUrl: buildOutlookAppComposeUrl({
        to: payload.to,
        cc: payload.cc,
        subject: payload.subject,
        body: payload.bodyText
      })
    };
  } catch (error) {
    // Log full error server-side, but keep user-facing fallback message concise.
    // eslint-disable-next-line no-console
    console.error(`Graph draft creation failed: ${error.message}`);
    return buildOutlookDraftFromPayload(
      payload,
      'Microsoft Graph draft is unavailable right now. Switched to Outlook deeplink fallback.'
    );
  }
}

function buildRecordMomShape(record) {
  return {
    meetingTitle: String(record?.meetingTitle || '').trim(),
    projectName: String(record?.projectName || '').trim(),
    projectNoWorkOrderNo: String(record?.projectNoWorkOrderNo || '').trim(),
    meetingDate: String(record?.meetingDate || '').trim(),
    meetingTime: String(record?.meetingTime || '').trim(),
    meetingLocation: String(record?.meetingLocation || '').trim()
  };
}

function getRecordById(recordId) {
  const { records, removed } = recordsStore.listRecords('');
  purgeRemovedRecordPdfs(removed);
  const targetId = String(recordId || '').trim();
  return (Array.isArray(records) ? records.find((item) => String(item.id || '') === targetId) : null) || null;
}

