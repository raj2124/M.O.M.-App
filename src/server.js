app.delete('/api/mom/records/:recordId', (req, res) => {
  try {
    const recordId = String(req.params.recordId || '').trim();
    const { deleted, record, removed } = recordsStore.deleteRecord(recordId);
    purgeRemovedRecordPdfs(removed);
    if (deleted && record?.pdfFileName) {
      safeUnlinkGeneratedPdf(record.pdfFileName);
    }

    if (!deleted) {
      return res.status(404).json({
        success: false,
        message: 'Record not found.'
      });
    }

    return res.json({
      success: true,
      message: 'Record deleted.',
      deletedRecordId: recordId
    });
  } catch (error) {
    return res.status(500).json({
      success: false,
      message: error.message || 'Failed to delete M.O.M record.'
    });
  }
});

app.post('/api/mom/records/:recordId/email-draft', async (req, res) => {
  try {
    const recordId = String(req.params.recordId || '').trim();
    const record = getRecordById(recordId);
    if (!record) {
      return res.status(404).json({
        success: false,
        message: 'Record not found.'
      });
    }

    const options = req.body.options || {};
    if (!options.sendEmail) {
      return res.status(400).json({
        success: false,
        message: 'Email export option is required.'
      });
    }

    const pdfUrl = String(record.pdfUrl || '').trim();
    if (!pdfUrl) {
      return res.status(400).json({
        success: false,
        message: 'PDF URL is unavailable for this record.'
      });
    }

    const emailDraft = await buildEmailDraft({
      mom: buildRecordMomShape(record),
      options,
      pdfUrl,
      pdfFileName: String(record.pdfFileName || '').trim()
    });

    return res.json({
      success: true,
      emailDraft
    });
  } catch (error) {
    return res.status(500).json({
      success: false,
      message: error.message || 'Failed to build email draft for record.'
    });
  }
});

app.post('/api/mom/submit', async (req, res) => {
  try {
    const mom = sanitizeMomPayload(req.body.mom || {});
    const options = req.body.options || {};

    const validationErrors = validateMomPayload(mom);
    if (validationErrors.length) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: validationErrors
      });
    }

    const shouldGeneratePdf = Boolean(options.generatePdf || options.printPdf || options.sendEmail);

    if (!shouldGeneratePdf) {
      return res.status(400).json({
        success: false,
        message: 'Select at least one output option (Email, PDF, Print).'
      });
    }

    const authenticity = buildAuthenticityMetadata();
    const { fileName } = await generateMomPdf(mom, config.app.generatedDir, authenticity);
    const pdfUrl = `/generated-pdfs/${fileName}`;

    let emailDraft = null;
    if (options.sendEmail) {
      emailDraft = await buildEmailDraft({
        mom,
        options,
        pdfUrl,
        pdfFileName: fileName
      });
    }

    const { record, removed } = recordsStore.addRecord({
      documentId: authenticity.documentId,
      meetingTitle: mom.meetingTitle,
      projectName: mom.projectName,
      projectNoWorkOrderNo: mom.projectNoWorkOrderNo,
      clientName: mom.clientName,
      meetingDate: mom.meetingDate,
      meetingTime: mom.meetingTime,
      meetingLocation: mom.meetingLocation,
      output: {
        generatePdf: Boolean(options.generatePdf),
        printPdf: Boolean(options.printPdf),
        sendEmail: Boolean(options.sendEmail)
      },
      pdfUrl,
      pdfFileName: fileName,
      pdfAbsoluteUrl: buildAbsolutePdfUrl(pdfUrl)
    });
    purgeRemovedRecordPdfs(removed);

    return res.json({
      success: true,
      message: 'M.O.M processed successfully.',
      result: {
        pdfUrl,
        pdfFileName: fileName,
        pdfAbsoluteUrl: buildAbsolutePdfUrl(pdfUrl),
        authenticity,
        emailSent: false,
        emailDraft,
        printRequested: Boolean(options.printPdf),
        record
      }
    });
  } catch (error) {
    return res.status(500).json({
      success: false,
      message: error.message || 'Failed to process M.O.M submission'
    });
  }
});

